"""
Python身份运算符
身份运算符用于比较两个对象的存储单元

运算符	描述	                                    实例
is  	is 是判断两个标识符是不是引用自一个对象	    x is y, 类似 id(x) == id(y) , 如果引用的是同一个对象则返回 True，否则返回 False
is not	is not 是判断两个标识符是不是引用自不同对象	x is not y ， 类似 id(a) != id(b)。如果引用的不是同一个对象则返回结果 True，否则返回 False。
"""
